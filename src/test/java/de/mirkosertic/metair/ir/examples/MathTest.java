package de.mirkosertic.metair.ir.examples;

import de.mirkosertic.metair.ir.test.MetaIRTest;

@MetaIRTest
public class MathTest {

    public int irem(final int a, final int b) {
        return a % b;
    }

    public long lrem(final long a, final long b) {
        return a % b;
    }

    public float frem(final float a, final float b) {
        return a % b;
    }

    public double drem(final double a, final double b) {
        return a % b;
    }

    public int idiv(final int a, final int b) {
        return a / b;
    }

    public long ldiv(final long a, final long b) {
        return a / b;
    }

    public float fdiv(final float a, final float b) {
        return a / b;
    }

    public double ddiv(final double a, final double b) {
        return a / b;
    }

    public int imul(final int a, final int b) {
        return a * b;
    }

    public long lmul(final long a, final long b) {
        return a * b;
    }

    public float fmul(final float a, final float b) {
        return a * b;
    }

    public double dmul(final double a, final double b) {
        return a * b;
    }

    public int iadd(final int a, final int b) {
        return a + b;
    }

    public long ladd(final long a, final long b) {
        return a + b;
    }

    public float fadd(final float a, final float b) {
        return a + b;
    }

    public double dadd(final double a, final double b) {
        return a + b;
    }

    public int isub(final int a, final int b) {
        return a - b;
    }

    public long lsub(final long a, final long b) {
        return a - b;
    }

    public float fsub(final float a, final float b) {
        return a - b;
    }

    public double dsub(final double a, final double b) {
        return a - b;
    }
}